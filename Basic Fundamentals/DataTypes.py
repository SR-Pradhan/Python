# Integers, -infinity to +inifinity
num = 56
print(num)
print(type(num)) # return type of variable

# Float
f = 1.57
print(f)
print(type(f)) 

# Booleans
isAdult = True
print(isAdult)
print(type(isAdult))

# String
name = "SR"
print(name)
print(type(name))

m = """ 
this is a multiline String
 """
print(m)
print(type(m))

# None
n = None
print(n)
